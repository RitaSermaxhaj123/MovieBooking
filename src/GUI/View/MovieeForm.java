    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        try{
            int row=table.getSelectedRow();
            if(row==-1){
                Movie m = new Movie();
                //p.setId(Integer.parseInt(idField.getText()));
                m.setMTitle(inpTitle.getText());
                m.setMType(inpType.getText());
                m.setMLanguage(inpLanguage.getText());
                Object value = jSpinner1.getValue();
                    if (value instanceof Date) {
                        Date date = (Date)value;
                        SimpleDateFormat format = new SimpleDateFormat("HH:mm:ss");
                        String time = format.format(date);
                        m.setMDuration(date);
                    }             
                m.setMDesc(inpDesc.getText());
                mr.create(m);
            }
            else{
                Movie m=mtm.getMovie(row);
                m.setMTitle(inpTitle.getText());
                m.setMType(inpType.getText());
                m.setMLanguage(inpLanguage.getText());
                Object value = jSpinner1.getValue();
                    if (value instanceof Date) {
                        Date date = (Date)value;
                        SimpleDateFormat format = new SimpleDateFormat("HH:mm:ss");
                        String time = format.format(date);
                        m.setMDuration(date);
                    }  
                m.setMDesc(inpDesc.getText());
                mr.edit(m);
            }
            clear();
            loadTable();
        }
        catch(MovieException e){
            JOptionPane.showMessageDialog(this, "E dhena ekziston");
        }
    }//GEN-LAST:event_btnSaveActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int row=table.getSelectedRow();
        if(row != -1){
            Object[] ob={"Po","Jo"};
            int i=JOptionPane.showOptionDialog(this,"A deshironi ta fshini?","Fshirja",JOptionPane.OK_OPTION,JOptionPane.QUESTION_MESSAGE, null,ob, ob[1]);
            if(i == 0){
                Movie m =mtm.getMovie(row);
                try{
                    mr.delete(m);
                } catch(MovieException e){
                    JOptionPane.showMessageDialog(this, e.getMessage() );
                }
                clear();
                loadTable();
            }
            else{
                clear();
            }
        }
        else{
            JOptionPane.showMessageDialog(this, "Nuk keni selektuar asgje per te fshire");
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

