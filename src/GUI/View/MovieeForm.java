    public void loadTable(){
        try{
            List<Movie> lista=mr.findAll();
            mtm.addList(lista);
            table.setModel(mtm);
            mtm.fireTableDataChanged();
        }
        catch(MovieException e){
            Logger.getLogger(MovieeForm.class.getName()).log(Level.SEVERE,null,e);
        }
    }
    
    private void tableSelectedIndexChange(){
        final ListSelectionModel rowSM = table.getSelectionModel();
        rowSM.addListSelectionListener(new ListSelectionListener(){

            @Override
            public void valueChanged(ListSelectionEvent e) {
                if(e.getValueIsAdjusting()){
                    return;
                }
                
                ListSelectionModel rowSM = (ListSelectionModel) e.getSource();
                int selectedIndex = rowSM.getAnchorSelectionIndex();
                if(selectedIndex > -1){
                    Movie m = mtm.getMovie(selectedIndex);
                    inpMovieID.setText(m.getMovieID().toString());
                    inpTitle.setText(m.getMTitle());
                    inpType.setText(m.getMType());
                    jSpinner1.setValue(m.getMDuration());
                    inpLanguage.setText(m.getMLanguage());
                    inpDesc.setText(m.getMDesc());
                }
            }
        });
    }
    

