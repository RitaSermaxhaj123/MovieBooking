    public void loadComboBox() {
        try {
            List<Roles> lista = rr.findAll();
            rcb.add(lista);
            roleCombo.setModel(rcb);
            roleCombo.repaint();
        } catch (MovieException ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }
    }
