    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try{
            UserRepository ur= new UserRepository();
            Users u= new Users();
            u.setEmail(usern.getText());
            u.setUpassword(new String(pass.getPassword()));
            u.setUserRole((Roles)roleCombo.getSelectedItem());
            ur.create(u);
            if(u!=null){
                JOptionPane.showMessageDialog(this, "Data successfully saved");
            }
        }
        catch(Exception ex){
            //            JOptionPane.showMessageDialog(this, "E dhena ekziston");
            System.out.println("Msg \n" + ex.getMessage());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

